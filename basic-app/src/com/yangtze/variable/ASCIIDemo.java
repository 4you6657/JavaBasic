package com.yangtze.variable;

public class ASCIIDemo {
    // 目标：掌握ASCII编码表的编码特点
    public static void main(String[] args) {
        System.out.println('a' + 10);   // 97 + 10 = 107
        System.out.println('A' + 10);   // 65 + 10 = 75
        System.out.println('0' + 10);   // 48 + 10 = 58
    }
}
